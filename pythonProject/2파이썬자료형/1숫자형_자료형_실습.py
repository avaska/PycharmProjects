
#숫자형 데이터는 어떻게 만들고 사용할까?

#1.정수형(Integer)


#실수형
a = 1.2
print(a)

a=-3.45
print(a)

#a라는 이름의 변수 메모리 공간에 컴퓨터 지수표현 방식으로
#4.24e10 또는 4.24E10 실수를 저장할 수 있다.
a = 4.24E10  #4.24 * 10의 10승의 값을 의미
print(a)

a = 4.24E-10   #4.24e-10은 4.24*10의 -10승의 값을 의미
print(a)


a = 0o177
print(a)

a = 0x8ff
print(a)

a = 0xABC
print(a)